#include "FileFolder.h"

//----------------------------------------------------------------------
//
//----------------------------------------------------------------------

FileFolder::FileFolder(QObject* parent) :
    QObject(parent)
{
}

//----------------------------------------------------------------------
//
//----------------------------------------------------------------------

FileFolder::FileFolder(const QString& path, QObject* parent) :
    QObject(parent)
{
    setPath(path);
}

//----------------------------------------------------------------------
//
//----------------------------------------------------------------------

void FileFolder::setUrl(const QUrl& url)
{
    if (m_Url == url)
    {
        return;
    }

    m_Url = url;
    m_Path = m_Url.toLocalFile();
    m_Dir = QDir(m_Path);

    emit fileFolderChanged();
}

//----------------------------------------------------------------------
//
//----------------------------------------------------------------------

void FileFolder::setPath(const QString& path)
{
    if (m_Path == path)
    {
        return;
    }

    m_Path = path;
    m_Url = QUrl::fromLocalFile(m_Path);
    m_Dir = QDir(m_Path);

    emit fileFolderChanged();
}

